package ewex.xml;

/**
* This class contains only static utility methods.
**/
//##################################################################
class XML{
//##################################################################

//===================================================================
static boolean isEmpty(ewe.util.Vector v)
//===================================================================
{
	return v.size() == 0;
}
//===================================================================
static Object lastElement(ewe.util.Vector v)
//===================================================================
{
	int idx = v.size()-1;
	if (idx < 0) return null;
	return v.get(idx);
}
//##################################################################
}
//##################################################################

