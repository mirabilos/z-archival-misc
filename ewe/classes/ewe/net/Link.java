package ewe.net;

/**
* A link is just a String that specifies a URL or hyperlink or even a File name.
* It is used to distinguish between text that specfies a Link and text that specifies
* data.
**/
//##################################################################
public class Link{
//##################################################################

String link;

//===================================================================
public Link(String link)
//===================================================================
{
	this.link = link;
	if (link == null) throw new NullPointerException();
}
//===================================================================
public String toString()
//===================================================================
{
	return link;
}
//===================================================================
public int hashCode()
//===================================================================
{
	return link.hashCode();
}

//##################################################################
}
//##################################################################

